primer_numero = float(input("Ingresa el primer número: "))
segundo_numero = float(input("Ingresa el segundo número: "))

suma = primer_numero + segundo_numero
print(f"El resultado de la suma es {suma}")

tercer_numero = float(input("Ingresa un tercer número: "))

resultado_multiplicacion = suma * tercer_numero
print(f"El resultado de la multiplicación es {resultado_multiplicacion}")
